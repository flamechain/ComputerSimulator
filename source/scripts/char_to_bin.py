def text_to_bin():
    binary = ''
    text = input()
    x = 2
    y = 2
    for i in text:
        for j in symbol_table:
            if symbol_table[j] == i:
                binary += j + '\n'
                new_x = '{0:b}'.format(x)
                while len(new_x) < 8:
                    new_x = '0' + new_x
                new_y = '{0:b}'.format(y)
                while len(new_y) < 8:
                    new_y = '0' + new_y
                binary += new_x + '\n' + new_y + '\n'
        x += 1

    print(binary)